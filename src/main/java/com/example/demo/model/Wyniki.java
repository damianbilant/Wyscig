package com.example.demo.model;


public class Wyniki {

    public Uczestnik uczestnik;
    public Double czasPrzejazduUczestnika;

    public Wyniki(Uczestnik uczestnik, Double czasPrzejazduUczestnika) {
        this.uczestnik = uczestnik;
        this.czasPrzejazduUczestnika = czasPrzejazduUczestnika;
    }

    public Uczestnik getUczestnik() {
        return uczestnik;
    }

}











